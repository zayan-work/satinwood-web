/**
 * Generates public/admin/config.yml for Sveltia CMS by introspecting the
 * content/*.json files, so every field is covered (Sveltia drops fields absent
 * from the schema on save) and field order matches the JSON. Re-run after
 * changing the shape of any content file: node scripts/gen-cms-config.mjs
 */
import { readFileSync, writeFileSync, readdirSync } from "node:fs";

const read = (p) => JSON.parse(readFileSync(p, "utf8"));

const humanize = (k) =>
  k
    .replace(/([a-z0-9])([A-Z])/g, "$1 $2")
    .replace(/[-_]/g, " ")
    .replace(/^\w/, (c) => c.toUpperCase());

// Fields that should be image pickers rather than plain text.
const IMAGE_KEYS = new Set(["photo"]);
// Long-form text fields by name (force multiline regardless of current length).
const TEXT_KEYS = new Set(["bio", "lede", "body", "a", "meta", "metaDescription", "quote", "outcome"]);

const isLong = (key, v) =>
  TEXT_KEYS.has(key) || (typeof v === "string" && (v.length > 80 || v.includes("\n")));

const pad = (n) => "  ".repeat(n);

// Emit a single field (key + value sample) as YAML lines at the given indent.
function field(key, value, indent, opts = {}) {
  const label = humanize(key);
  const head = `${pad(indent)}- name: ${key}\n${pad(indent + 1)}label: ${JSON.stringify(label)}\n`;
  // A field is optional when caller says so, or when its seed value is an empty
  // string — Sveltia treats string fields as required by default, so a legitimately
  // empty seed (e.g. stats prefix, a plain-text ledePart href) would block saving.
  const optional =
    opts.required === false ||
    (opts.required === undefined && typeof value === "string" && value.trim() === "");
  const req = optional ? `${pad(indent + 1)}required: false\n` : "";

  if (IMAGE_KEYS.has(key)) return head + req + `${pad(indent + 1)}widget: image\n`;

  if (Array.isArray(value)) {
    if (value.length === 0) {
      // unknown element shape — fall back to an editable text list
      return head + req + `${pad(indent + 1)}widget: list\n`;
    }
    if (typeof value[0] === "object" && value[0] !== null) {
      // list of objects — union keys across all elements
      const keys = [];
      const seen = new Set();
      for (const el of value)
        for (const k of Object.keys(el))
          if (!seen.has(k)) {
            seen.add(k);
            keys.push(k);
          }
      let out = head + req + `${pad(indent + 1)}widget: list\n`;
      out += `${pad(indent + 1)}fields:\n`;
      for (const k of keys) {
        const present = value.find((el) => el[k] !== undefined && el[k] !== "");
        const sample = present ? present[k] : value.find((el) => el[k] !== undefined)[k];
        // Optional if any entry is missing the key or has it empty.
        const required = value.some((el) => el[k] === undefined || el[k] === "") ? false : undefined;
        out += field(k, sample, indent + 2, { required });
      }
      return out;
    }
    // list of scalars
    const scalarWidget = typeof value[0] === "number" ? "number" : isLong(key, value[0]) ? "text" : "string";
    return (
      head +
      req +
      `${pad(indent + 1)}widget: list\n` +
      `${pad(indent + 1)}field: { name: item, label: Item, widget: ${scalarWidget} }\n`
    );
  }

  if (value !== null && typeof value === "object") {
    let out = head + req + `${pad(indent + 1)}widget: object\n${pad(indent + 1)}fields:\n`;
    for (const [k, v] of Object.entries(value)) out += field(k, v, indent + 2);
    return out;
  }

  if (typeof value === "number") return head + req + `${pad(indent + 1)}widget: number\n`;
  if (typeof value === "boolean") return head + req + `${pad(indent + 1)}widget: boolean\n`;
  return head + req + `${pad(indent + 1)}widget: ${isLong(key, value) ? "text" : "string"}\n`;
}

// Build the field list for an object's top-level keys.
function fieldsFor(obj, indent) {
  return Object.entries(obj)
    .map(([k, v]) => field(k, v, indent))
    .join("");
}

/* ----------------------------------------------------------------- header */
let yml = `# Sveltia CMS configuration — generated by scripts/gen-cms-config.mjs.
# Edit the generator, not this file. Docs: https://sveltiacms.app/en/docs
#
# Auth: GitHub backend. Editors click "Sign In with Token" (a GitHub personal
# access token, repo scope) — no extra infrastructure needed. For local editing,
# run \`pnpm dev\`, open http://localhost:3000/admin/ in a Chromium browser, and
# choose "Work with Local Repository".
backend:
  name: github
  repo: zayan-work/satinwood-web
  branch: main

# New media uploads are committed here and referenced at /media/... . Existing
# images under /public (e.g. /team/*.jpg) keep working as-is.
media_folder: public/media
public_folder: /media

collections:
  - name: pages
    label: Pages
    description: One-off pages — edit the copy for each section.
    files:
`;

/* ------------------------------------------------------- file collection */
const pages = [
  ["home", "Home page", "content/home.json"],
  ["global", "Navigation & footer", "content/global.json"],
  ["team", "Team page", "content/team.json"],
  ["advisory", "Advisory page", "content/advisory.json"],
];
for (const [name, label, file] of pages) {
  const data = read(file);
  yml += `${pad(3)}- name: ${name}\n${pad(4)}label: ${JSON.stringify(label)}\n${pad(4)}file: ${file}\n`;
  yml += `${pad(4)}fields:\n`;
  yml += fieldsFor(data, 5);
}

/* ----------------------------------------------------- folder collection */
// Union field shape across all service files so nothing is dropped.
const serviceFiles = readdirSync("content/services").filter((f) => f.endsWith(".json"));
const merged = {};
for (const f of serviceFiles) Object.assign(merged, read(`content/services/${f}`));
yml += `
  - name: services
    label: Service pages
    description: SEO landing pages (one file per service).
    folder: content/services
    extension: json
    format: json
    create: true
    identifier_field: serviceName
    slug: "{{fields.slug}}"
    summary: "{{fields.serviceName}}"
    fields:
`;
yml += fieldsFor(merged, 3);

writeFileSync("public/admin/config.yml", yml);
console.log("wrote public/admin/config.yml (" + yml.split("\n").length + " lines)");
